public class Holidays {

    private String Day;
    private String Date;

    public Holidays(String Day, String Date) {
        this.Day = Day;
        this.Date = Date;
    }

    public String getDay() {
        return Day;
    }

    public String getDate() {
        return Date;
    }
}
